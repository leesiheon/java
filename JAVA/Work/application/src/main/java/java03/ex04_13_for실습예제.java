package java03;
 
public class ex04_13_for실습예제 {
    public static void main(String[] args) {
        
        for (int i = 9; i <= 1; i = i - 1) {
            
            int x = 2 * i;
            
            System.out.println("곱하기: " + 2 + "*" + i + "=" + x );
            
        }
        
    }
    
}